package nl.datasteel.crudcraft.sample;

import jakarta.servlet.FilterChain;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.io.IOException;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Component;
import org.springframework.web.filter.OncePerRequestFilter;

/**
 * Simple servlet filter that logs how long each HTTP request takes. Included to
 * show how custom infrastructure can live alongside CrudCraft endpoints.
 */
@Component
public class RequestTimingFilter extends OncePerRequestFilter {

    /** Logger used to output request duration information. */
    private static final Logger log = LoggerFactory.getLogger(RequestTimingFilter.class);

    @Override
    protected void doFilterInternal(HttpServletRequest request,
                                    HttpServletResponse response,
                                    FilterChain filterChain)
            throws ServletException, IOException {

        long start = System.currentTimeMillis();
        filterChain.doFilter(request, response);
        long duration = System.currentTimeMillis() - start;

        log.info("{} {} â†’ {} ms",
                request.getMethod(),
                request.getRequestURI(),
                duration);
    }
}
